                    <p class="projText nextPage">In 2002 DesignMap began working with Cisco to design Cisco.com, establish standards for Cisco's teams to use on their own part of the site, and put in place a process to support those teams, maintain consistency, and maintain and extend the standards. </p>

					<p class="projText nextPage">In 2005, Cisco asked us to do the same for their applications. Our involvement is threefold: </p>
					<p class="projText nextPage">First: worked with Cisco to identify existing best practices, extend them consistently, and fill in the gaps. Then we verified our work and socialized it broadly, recognizing that to be successful the standards had to be embraced by the people using them. Finally, we delivered comprehensive guidelines as a site that could be hosted on Cisco's intranet. Our standards included guidelines at every level: architecture, interaction, and presentation across processes, frameworks, components, and user interface controls and content.</p>
                  </div>
