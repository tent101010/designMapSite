					<p class="projText prevPage">Second: manage and oversee the application of the standards. To that end, we work with Cisco's internal teams or external vendors as they need us. At the most basic level, we simply review the work and make recommendations if they are needed. We can also contribute a few pieces to help establish direction within the framework of the guidelines. Or we can act as leads for the design team throughout product design and development. </p>
					<p class="projText prevPage">Third: design applications ourselves. Since the standards were created, we've wholly designed dozens of Cisco products, and been expert User Experience (UE) consultants on dozens more. </p>
					<p class="projText prevPage">We think the most important comment on our work with Cisco is that our engagement with Cisco continues to this day, six years later.</p>
